Map::Map()
{

	dirt = TextureManager::LoadTexture("assets/dirt.png");//0
	grass = TextureManager::LoadTexture("assets/grass.png");//1
	water = TextureManager::LoadTexture("assets/water.png");//2

	LoadMap(lvl1);

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;

	dest.x = dest.y = 0;

}

void Map::LoadMap(int arr[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int col = 0; col < 25; col++)
		{
			map[row][col] = arr[row][col];
		}
	}
}

Map::~Map()
{
	SDL_DestroyTexture(grass);
	SDL_DestroyTexture(water);
	SDL_DestroyTexture(dirt);
}

void Map::DrawMap()
{
	int type = 0;

	for (int row = 0; row < 20; row++)
	{
		for (int col = 0; col < 25; col++)
		{
			type = map[row][col];

			dest.x = col * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(dirt, src, dest, SDL_FLIP_NONE);
				break;
			case 1:
				TextureManager::Draw(grass, src, dest, SDL_FLIP_NONE);
				break;
			case 2:
				TextureManager::Draw(water, src, dest, SDL_FLIP_NONE);
				break;
			}
		}
	}
}